	<div class="box box3">
		<div class="item">
			<div class="day-title row-s">D01.抵达拉萨，我社安排接机</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:1h<br>KM:60<br>海拔:3700M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>指定航班自行购买，成都机场与团员汇合后统一办理登机牌。司机导游会在拉萨贡嘎机场接机，后乘车沿拉萨河风景带前往拉萨市区，感受高原纯净的空气。到达拉萨后统一入住旅行社订好的酒店,到达拉萨后尽量避免外出，好好休息，以适应高原反应。<br>温馨提示：拉萨海拔3650米，含氧量只有内地的70%，前2天多数客人会出现不同程度的高原反应。保持平常心态以外，多饮水、多吃水果、充分的休息是预防高原反应的最佳办法。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>拉薩國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D02.拉萨市内：布达拉宫、大昭寺、八角街自由行</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:0.5h<br>KM:20<br>海拔:3700M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>上午游览世界上海拔最高的古代宫堡式建筑群――布达拉宫，历世达赖喇嘛驻地和西藏政教权力的中心；珍藏有存放历代达赖喇嘛真身的金质灵塔和大量珍贵的壁画、唐卡、佛像，是藏民族文化的集中体现。<br> 午餐后游览参观建于吐蕃王朝时期的最早的佛堂——大昭寺，寺中供奉有随行文成公主入藏的释迦牟尼十二岁等身像，是藏族老百姓心中最为神圣灵验的一尊佛像，在这里游客不仅能够看到老百姓朝拜的虔诚，还可随老百姓一起围绕八角街转经为众生祈祷。 温馨提示：在西藏进寺院时记得脱帽、摘掉墨镜、不要吸烟、摸佛像、翻经书、敲钟鼓等。对于其他的一些佛教的器物，例如佛珠、护身符等，不能随便乱摸。有些寺庙不能拍照，要遵守这些规定。

			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>拉薩國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D03.拉萨市内、罗布林卡、色拉寺</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:1h<br>KM:30<br>海拔:3700M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>罗布林卡属全国重点文物保护单位，位于西藏拉萨西郊。始建于十八世纪40年代（达赖七世），是历代达赖喇嘛消夏理政的地方，是一座典型的藏式风格园林。经过二百多年的扩建，全园占地36万平方米，园内有植物100余种，不仅有拉萨地区常见花木，而且有取自喜马拉雅山南北麓的奇花异草，还有从内地移植或从国外引进的名贵花卉，堪称高原植物园。建筑以格桑颇章、金色颇章、达登明久颇章为主体，有房374间，是西藏人造园林中规模最大、风景最佳的、古迹最多的园林。罗布林卡意为宝贝园林。<br> 色拉寺坐落在拉萨北郊山下，是藏传佛教格鲁派三大寺之一。建于明永乐十七年(1419)。色拉寺藏语为“野玫园寺”。色拉寺建有一个大殿、三个扎仓(僧院)、二十九个康村。僧人最多时有5500多人。寺内保存着上万个金刚佛像，大多为西藏本地制作。还有许多是从内地或印度带来的铜佛像。大殿和各扎仓经堂四壁保存着大量彩色壁画原作。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>拉薩國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D04.拉萨、巴松错、尼洋河、林芝</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:8h<br>KM:560<br>海拔:巴松措3700M、林芝3100M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>拉萨乘车前往位于工布江达县境内的巴松措湖，沿尼洋河东行，翻越5013米的米拉山口，驰向海拔仅2800米西藏江南林芝，沿途景色秀美，藏民族村落呈现自己独有的特色和迷人的景观。可以停车拍照。<br>下午游览西藏最美丽的湖泊、雪山森林环绕中被称为小瑞士的—巴松错。上湖心小岛观赏,凭栏远眺巴松错的湖光山色。<br>之后驱车至林芝，藏语为“太阳的宝座”。沿途欣赏尼洋河河谷风光，原始森林，感受真正的山清水秀。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>林芝國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D05.林芝、南迦巴瓦峰、鲁朗林海</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:7.5h<br>KM:320<br>海拔:魯朗林海3700M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>早起，从林芝出发到鲁朗约100公里，行程2小时。途中翻越海拔4515米的色季拉山口，沿途欣赏色季拉彩林，也可以远眺林芝县城。达到山口，是远眺南迦巴瓦峰的绝佳处。也是拍摄南迦巴瓦峰最好的地点。<br> 参观完毕后前往鲁朗林海，鲁朗--藏语是神仙居住的地方。远处终年不化的雪山冰峰、近处的茫茫林海。宛如世外桃源，又似人间仙境，使人久久不愿离去。之后下鲁朗牧场，牧场一派田园风光。山上原始森林，山脚下是高山牧场，其间牛羊点缀。眼前是藏式木头房子，仿佛置身于画境。中午还可在鲁朗牧场品尝当地特色--鲁朗石锅鸡。参观完毕后返回林芝。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>林芝國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D06.林芝、卡定沟天佛瀑布、拉萨</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:8h<br>KM:480<br>海拔:卡定溝2980M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>早餐后前去意为“天上人间”的原始森林－【卡定沟天佛瀑布】。距八一镇24公里处的尼洋河畔卡定沟内，是典型的峡谷地貌，山高沟深，奇峰异石，古树参天，落差近200米的瀑布飞流直下，雄伟壮观，瀑布中一尊天然形成的大佛面目慈祥，天佛瀑布由此而得名。岩壁上有自然形成佛像与守护者，沟谷中原始森林密布，有与藏传佛教浑然一体的优美传说。<br>游览结束后驱车沿原路返回拉萨。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>拉薩國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D07.拉萨、羊湖、江孜、日喀则</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:8h<br>KM:380<br>海拔:羊湖4500M、日喀則3850M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>早晨从拉萨启程，驱车前往色彩炫丽多变西藏三大圣湖之一圣湖-羊卓雍错。羊湖是世界上最高的淡水湖，湖面海拔4441米；藏语为“碧玉湖”，之后下山零距离接触羊卓雍错。湛蓝的湖水与远方的雪山连为一体，加上两岸各色的山脉、极具特色的藏民族村落、遍地的牛羊，一路走来，心旷神怡。<br>下午途经远观卡若拉冰川：感受她的巍峨壮观、云雾飘缈、绝世容颜，冰川是流动的岁月和冰雪写就的地质历史，是时光雕塑的伟大艺术品。之后抵达西藏的英雄城--江孜县，远眺江孜古堡；沿江孜平原到达后藏重镇--日喀则。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>日喀則國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D08.日喀则、拉孜、定日、珠峰大本营</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:8h<br>KM:380<br>海拔:珠峰大本營5300M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>早上乘车途径拉孜前往新定日县城－协格尔。沿途你将翻越海拔5220米的嘉措拉雪山，在将到协格尔时，即可远眺珠穆朗玛峰的雄姿。沿途你将翻越海拔5220米的嘉措拉雪山山口，稍做停留后赶往定日。<br>下午抵达世界上海拔最高的珠峰自然保护区。在世界上海拔最高的寺庙—绒布寺南望，是举世公认仰望珠峰的最佳位置。5100米处的寺庙，白塔耸立，五彩经幡翻飞。乘坐环保车到达海拔5200米的珠峰大本营，在此您将与世界最高峰珠穆朗玛峰共舞，有幸还可以看到有如生命之火般飘荡的珠峰旗云。<br>温馨提示：夜宿珠峰民宿，天气好的话晚上可观赏星辰银河，带好单反相机，享受视觉盛宴。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>珠峰大本營露營帳篷</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D09.珠峰大本营、定日、日喀则</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:8h<br>KM:380<br>海拔:珠峰大本營5300M、日喀則3850M</div>
			<div class="day-content row-s"><i><img src="https://picture.tcc.cn/chimera/details/scenic-spot-icon.svg" /></i>清晨在地球之巅迎着阳光仰望珠峰，峰顶终年积雪，远望冰川悬垂，银峰高耸，一派圣洁景象。您能感到生命被荒芜，人为寂静所征服，在宇宙的纯与净中变得渺小而又明朗。<br>带着对珠峰的留恋，在巍峨壮丽、气势磅礴、蜿蜒千里的喜马拉雅山脉下午返回日喀则。
			</div>
			<div class="day-jiu row-s"><i><img src="https://picture.tcc.cn/chimera/details/hotelicon.svg" /></i>日喀則國際三星級酒店(客人可指定)</div>
		</div>

		<div class="item">
			<div class="day-title row-s">D10.日喀则、扎什伦布寺、中尼公路、雅鲁藏布江河谷、尼木(大竹卡)、藏北草原、当雄县、念青唐古拉山、纳木措</div>
			<div class="day-km row-s"><i><img src="https://picture.tcc.cn/chimera/details/trip1-icon.svg" /></i>车:8.5h<br>KM:450<br>海拔:日喀則3850M、納木錯4710M</div>
